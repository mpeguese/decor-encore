export function normalizeZip(zip: string) {
  return zip.trim().replace(/\D/g, "").slice(0, 5)
}

export function getZipCoordinates(zip: string): Coordinates | null {
  const cleanZip = normalizeZip(zip)
  return ZIP_COORDINATES[cleanZip] || null
}

export function getDistanceMiles(from: Coordinates, to: Coordinates) {
  const earthRadiusMiles = 3958.8

  const fromLat = (from.lat * Math.PI) / 180
  const toLat = (to.lat * Math.PI) / 180
  const deltaLat = ((to.lat - from.lat) * Math.PI) / 180
  const deltaLng = ((to.lng - from.lng) * Math.PI) / 180

  const a =
    Math.sin(deltaLat / 2) * Math.sin(deltaLat / 2) +
    Math.cos(fromLat) *
      Math.cos(toLat) *
      Math.sin(deltaLng / 2) *
      Math.sin(deltaLng / 2)

  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))

  return earthRadiusMiles * c
}